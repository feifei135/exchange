                            <p>（二）  风险警示制度：当交易中心认为必要时，可以分别或同时采取：通过电话或约见方式实施谈话提醒、对部分或者全部交易商发布风险提示函等，警示和化解风险，维护交易商合法权益。风险警示制度的措施，不得作为对具体价格走势的暗示或者干涉；</p>
